class Obstacle:

    def __init__(self, x, y, type):
        self.blocks_group = pygame.sprite.Group()
        # loop over all elements of the grid
        if type == HolidayType.HALLOWEEN:
            for row in range(len(halloween_grid)):
                for column in range(len(halloween_grid[0])):
                    if halloween_grid[row][column] == 1:
                        pos_x = x + column * BLOCK_PIXEL_WIDTH
                        pos_y = y + row * BLOCK_PIXEL_HEIGHT
                        block = Block(pos_x, pos_y, (146,146,146))
                        self.blocks_group.add(block)
        elif type == HolidayType.THANKSGIVING:
            for row in range(len(thanksgiving_grid)):
                for column in range(len(thanksgiving_grid[0])):
                    if thanksgiving_grid[row][column] == 1:
                        pos_x = x + column * BLOCK_PIXEL_WIDTH
                        pos_y = y + row * BLOCK_PIXEL_HEIGHT
                        block = Block(pos_x, pos_y, (243, 216, 63))
                        self.blocks_group.add(block)
        elif type == HolidayType.CHRISTMAS:
            for row in range(len(christmas_grid)):
                for column in range(len(christmas_grid[0])):
                    if christmas_grid[row][column] == 1:
                        pos_x = x + column * BLOCK_PIXEL_WIDTH
                        pos_y = y + row * BLOCK_PIXEL_HEIGHT
                        block = Block(pos_x, pos_y, (219, 62, 42))
                        self.blocks_group.add(block)
